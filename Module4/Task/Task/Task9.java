public class Task9
{
        public static void main(String[] args) {
            // Loop through numbers 1 to 10
            for (int i = 1; i <= 10; i++) {
                // Check if the number is odd or even
                if (i % 2 == 0) {
                    System.out.println(i + " is even");
                } else {
                    System.out.println(i + " is odd");
                }
            }
        }
    }




