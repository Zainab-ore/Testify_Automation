""""
Create a class called Human
Add an attribute leg_count with the value of 4
Add another attribute can_walk with value of True

Optionally you can instantiate the class and prints out the leg_count and can_walk attributes"""
class Human:
    leg_count = 4
    can_walk = True

    def __init__(result):
        result.leg_count = 4
        result.can_walk = True


# Optionally you can instantiate the class and prints out the leg_count and can_walk attributes.
human_sample = Human()

print("Output:" , human_sample.leg_count, " and ", human_sample.can_walk)